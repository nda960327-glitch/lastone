// Fisher-Yates 셔플 알고리즘
function shuffle(array) {
  for (let i = array.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [array[i], array[j]] = [array[j], array[i]];
  }
  return array;
}

// 단어 입력 파싱
function parseWords(text) {
  const lines = text.split('\n');
  const list = [];
  
  for (let line of lines) {
    line = line.trim();
    if (!line) continue;

    const parts = line.split(/(\[[a-zA-Z가-힣]+\])/);
    if (parts.length < 3) continue;

    const word = parts[0].trim();
    const meanings = [];

    for (let i = 1; i < parts.length; i += 2) {
      const rawPos = parts[i];
      const rawMeaning = parts[i + 1];

      if (!rawPos || !rawMeaning) continue;

      const pos = rawPos.replace('[', '').replace(']', '').trim();
      
      let meaning = rawMeaning.trim();
      if (meaning.startsWith('/')) {
        meaning = meaning.substring(1).trim();
      }
      if (meaning.endsWith('/')) {
        meaning = meaning.slice(0, -1).trim();
      }
      
      meanings.push({ pos, meaning });
    }

    if (word && meanings.length > 0) {
      list.push({
        word,
        meanings,
        passed: false,
        attempts: 0
      });
    }
  }
  return list;
}

