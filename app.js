// HTML 이스케이프
function esc(str) {
  return str.replace(/&/g,'&amp;').replace(/</g,'&lt;').replace(/>/g,'&gt;');
}

// 뜻 HTML 생성
function meaningHTML(meanings) {
  return meanings.map(m =>
    `<div class="meaning-line">
       <span class="pos-badge">[${esc(m.pos)}]</span>
       <span>${esc(m.meaning)}</span>
     </div>`
  ).join('');
}

