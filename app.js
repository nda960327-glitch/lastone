// 로컬 저장소 기반 단어 리스트 로드 및 구버전 데이터 강제 마이그레이션
function loadDBList(textarea) {
  const statusEl = document.getElementById('worddb-status');
  
  // 마이그레이션 버전 3 키를 검사하여 신규 데이터셋(Day 1 ~ Day 4) 강제 동기화
  const isMigrated = localStorage.getItem('vocab_db_version_3');
  if (!isMigrated) {
    localStorage.removeItem('vocab_db_initialized');
    localStorage.removeItem('vocab_file_기초 영단어 Day 1');
    localStorage.removeItem('vocab_file_기초 영단어 Day 2');
    localStorage.removeItem('vocab_file_기초 영단어 Day 3');
    localStorage.removeItem('vocab_file_기초 영단어 Day 4');
    localStorage.setItem('vocab_db_version_3', 'true');
  }

  // 최초 로드 또는 마이그레이션 리셋 시 기본 단어 세트 저장소에 등록
  if (!localStorage.getItem('vocab_db_initialized')) {
    for (const [title, content] of Object.entries(DEFAULT_DATABASES)) {
      localStorage.setItem(`vocab_file_${title}`, content);
    }
    localStorage.setItem('vocab_db_initialized', 'true');
  }

  if (statusEl) {
    statusEl.innerHTML = 'ℹ️ <b>웹 보관함 단어장</b>';
    statusEl.style.color = 'var(--text2)';
  }

  refreshDBList(textarea);
}

// 저장된 단어 목록을 드롭다운(Select Box)에 갱신하여 렌더링
function refreshDBList(textarea) {
  const selectEl = document.getElementById('worddb-select');
  const statusEl = document.getElementById('worddb-status');
  
  if (!selectEl) return;

  selectEl.innerHTML = '<option value="">📂 내장 단어장 선택하기...</option>';

  const keys = [];
  for (let i = 0; i < localStorage.length; i++) {
    const key = localStorage.key(i);
    if (key.startsWith('vocab_file_')) {
      keys.push(key.replace('vocab_file_', ''));
    }
  }

  keys.sort((a, b) => a.localeCompare(b, 'ko'));

  if (keys.length === 0) {
    if (statusEl) statusEl.textContent = '등록된 단어장이 없습니다. 파일을 추가해 주세요.';
    return;
  }

  keys.forEach(title => {
    const opt = document.createElement('option');
    opt.value = title;
    opt.textContent = `📖 ${title}`;
    selectEl.appendChild(opt);
  });

  selectEl.onchange = (e) => {
    const selectedTitle = e.target.value;
    if (!selectedTitle) return;

    const content = localStorage.getItem(`vocab_file_${selectedTitle}`);
    if (content) {
      textarea.value = content.trim();
      textarea.dispatchEvent(new Event('input'));
    }
  };

  // 선택한 단어장 삭제 버튼
  let deleteBtn = document.getElementById('btn-delete-selected-db');
  if (!deleteBtn) {
    deleteBtn = document.createElement('button');
    deleteBtn.id = 'btn-delete-selected-db';
    deleteBtn.className = 'btn-ghost';
    deleteBtn.style.cssText = 'border: none; background: transparent; color: var(--red); font-size: 11px; text-decoration: underline; margin-top: 8px; cursor: pointer; display: block; width: 100%; text-align: right;';
    deleteBtn.textContent = '🗑️ 선택한 단어장 삭제';
    selectEl.parentNode.appendChild(deleteBtn);
  }

  deleteBtn.onclick = () => {
    const selectedTitle = selectEl.value;
    if (!selectedTitle) {
      alert('삭제할 단어장을 먼저 목록에서 선택해 주세요.');
      return;
    }
    if (confirm(`"${selectedTitle}" 단어장을 삭제하시겠습니까?\n(기본 내장 단어장인 경우 새로고침 시 복구됩니다)`)) {
      localStorage.removeItem(`vocab_file_${selectedTitle}`);
      selectEl.value = '';
      refreshDBList(textarea);
      textarea.value = '';
      textarea.dispatchEvent(new Event('input'));
    }
  };
}

